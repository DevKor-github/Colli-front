export const getCurrentMonthDate = (month: number) => {
  const lastDate = new Date(2023, month, 0).getDate()
  const days = Array.from(
    { length: lastDate },
    (_, i) => `${i + 1}일 ${DAYS[new Date(2023, month - 1, i + 1).getDay()]}`
  )
  return days
}
